// ─── Email Modal ─────────────────────────────────────────
function EmailModal({ formData, prompts, onClose }) {
  const [email, setEmail] = useState('');
  const [emailError, setEmailError] = useState(false);
  const [sent, setSent] = useState(false);

  const send = () => {
    if (!email || !email.includes('@')) { setEmailError(true); return; }
    const subject  = encodeURIComponent(`Your Dream 100 Blueprint — ${formData.niche} in ${formData.geo}`);
    const bodyText = `Hi ${formData.name},\n\nHere is your Dream 100 Partner Blueprint for ${formData.niche} in ${formData.geo}.\n\n`
      + prompts.map((p, i) => `PHASE ${i + 1}: ${p.title}\nFor: ${p.model}\n\n${p.prompt}`).join('\n\n---\n\n')
      + `\n\n---\nGenerated by NurturInk Dream 100 Builder\nhttps://pipeline.nurturink.com\n\nLearn how NurturInk can help you land these partners:\nhttps://nurturink.com/realestate`;
    window.open(`mailto:${email}?subject=${subject}&body=${encodeURIComponent(bodyText)}`);
    setSent(true);
  };

  return (
    <div
      onClick={e => { if (e.target === e.currentTarget) onClose(); }}
      style={{
        position: 'fixed', inset: 0,
        background: 'rgba(15,20,40,0.6)', backdropFilter: 'blur(5px)',
        zIndex: 200, display: 'flex', alignItems: 'flex-end', justifyContent: 'center',
      }}
    >
      <div style={{
        background: 'var(--d100-white)',
        borderRadius: '20px 20px 0 0',
        padding: '28px 22px 36px',
        width: '100%', maxWidth: 600,
        boxShadow: 'var(--d100-shadow-lg)',
        animation: 'd100-slideUp 0.28s ease both',
      }}>
        {/* Handle */}
        <div style={{ width: 36, height: 4, background: 'var(--d100-border)', borderRadius: 2, margin: '0 auto 20px' }} />

        {!sent ? (
          <>
            <h3 style={{ fontSize: 20, fontWeight: 800, color: 'var(--d100-navy)', marginBottom: 6 }}>Email Me My Blueprint</h3>
            <p style={{ fontSize: 15, color: 'var(--d100-text-muted)', marginBottom: 18, lineHeight: 1.5 }}>
              We'll send your complete 7-phase blueprint to your inbox. No spam — one email, just your blueprint.
            </p>
            <input
              type="email" value={email}
              onChange={e => { setEmail(e.target.value); setEmailError(false); }}
              placeholder="your@email.com"
              style={{
                width: '100%', padding: '14px 16px',
                border: `1.5px solid ${emailError ? '#DC2626' : 'var(--d100-border)'}`,
                borderRadius: 10, fontFamily: "'Sora', sans-serif", fontSize: 16,
                color: 'var(--d100-text)', background: 'var(--d100-cream)',
                outline: 'none', marginBottom: 12,
              }}
            />
            <button
              onClick={send}
              style={{
                width: '100%', padding: 15,
                background: 'var(--d100-gold)', color: 'var(--d100-navy)', border: 'none',
                borderRadius: 10, fontFamily: "'Sora', sans-serif",
                fontSize: 16, fontWeight: 800, cursor: 'pointer', marginBottom: 10,
              }}
            >Send My Blueprint</button>
            <button onClick={onClose} style={{
              width: '100%', padding: 13,
              border: '1.5px solid var(--d100-border)', background: 'transparent',
              borderRadius: 10, fontFamily: "'Sora', sans-serif",
              fontSize: 15, color: 'var(--d100-text-muted)', cursor: 'pointer', fontWeight: 500,
            }}>Cancel</button>
            <p style={{ fontSize: 11, color: 'var(--d100-text-muted)', textAlign: 'center', marginTop: 10, lineHeight: 1.5 }}>
              Your email is used only to deliver your blueprint. We may occasionally share helpful resources from NurturInk. Unsubscribe anytime.
            </p>
          </>
        ) : (
          <div style={{ textAlign: 'center', padding: '12px 0' }}>
            <div style={{ fontSize: 44, marginBottom: 14 }}>✉️</div>
            <h3 style={{ fontSize: 20, fontWeight: 800, color: 'var(--d100-navy)', marginBottom: 8 }}>Check Your Inbox</h3>
            <p style={{ fontSize: 15, color: 'var(--d100-text-muted)', marginBottom: 20, lineHeight: 1.5 }}>
              Your blueprint is on its way. While you're here — see how NurturInk can help you land those partners faster.
            </p>
            <a href="https://nurturink.com/realestate" target="_blank" rel="noreferrer" style={{
              display: 'block', background: 'var(--d100-gold)', color: 'var(--d100-navy)',
              textDecoration: 'none', fontWeight: 800, fontSize: 15,
              padding: 14, borderRadius: 10, marginBottom: 12,
            }}>See NurturInk for Agents →</a>
            <button onClick={onClose} style={{
              width: '100%', padding: 13,
              border: '1.5px solid var(--d100-border)', background: 'transparent',
              borderRadius: 10, fontFamily: "'Sora', sans-serif",
              fontSize: 15, color: 'var(--d100-text-muted)', cursor: 'pointer', fontWeight: 500,
            }}>Close</button>
          </div>
        )}
      </div>
    </div>
  );
}

