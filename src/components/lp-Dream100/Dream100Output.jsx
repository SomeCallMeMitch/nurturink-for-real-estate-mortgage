const outlineBtn = {
  flex: 1, padding: 13, background: 'transparent',
  color: 'var(--d100-navy)', border: '1.5px solid var(--d100-navy)',
  borderRadius: 10, fontFamily: "'Sora', sans-serif",
  fontSize: 15, fontWeight: 600, cursor: 'pointer', transition: 'all 0.2s',
};